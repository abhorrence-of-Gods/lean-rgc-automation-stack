        DefectAtom("missing_and_projection", "premise", "has_and_hyp", ["simp_all", "exact h.left", "exact h.right"], description="conjunctive hypothesis needs projection"),
        DefectAtom("eq_reflexive_goal", "goal", "target_is_eq", ["rfl", "simp"], description="equality/reflexive-style goal"),
        DefectAtom("nat_arith_goal", "method", "has_nat_arith", ["omega", "norm_num", "simp"], description="Nat/Int arithmetic carrier"),
        DefectAtom("list_simp_goal", "method", "has_list", ["simp"], description="List simplification carrier"),
