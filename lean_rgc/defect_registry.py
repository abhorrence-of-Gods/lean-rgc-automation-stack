from __future__ import annotations

from dataclasses import dataclass, asdict, field
from pathlib import Path
from typing import Any
import json


@dataclass
class DefectAtom:
    atom_id: str
    group: str
    detector: str
    intervention_templates: list[str] = field(default_factory=list)
    status: str = "active"
    evidence: dict[str, Any] = field(default_factory=dict)
    description: str = ""

    def to_dict(self) -> dict[str, Any]:
        return asdict(self)


@dataclass
class DefectRegistry:
    atoms: list[DefectAtom] = field(default_factory=list)
    version: str = "lean-rgc-defect-registry-v0.1"
    metadata: dict[str, Any] = field(default_factory=dict)

    def to_dict(self) -> dict[str, Any]:
        return {"version": self.version, "metadata": self.metadata, "atoms": [a.to_dict() for a in self.atoms]}

    @classmethod
    def from_dict(cls, d: dict[str, Any]) -> "DefectRegistry":
        return cls(atoms=[DefectAtom(**a) for a in d.get("atoms", [])], version=d.get("version", "lean-rgc-defect-registry-v0.1"), metadata=d.get("metadata", {}))

    def save(self, path: str | Path) -> None:
        path = Path(path); path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(json.dumps(self.to_dict(), indent=2, ensure_ascii=True, sort_keys=True), encoding="utf-8")

    @classmethod
    def load(cls, path: str | Path) -> "DefectRegistry":
        return cls.from_dict(json.loads(Path(path).read_text(encoding="utf-8")))


